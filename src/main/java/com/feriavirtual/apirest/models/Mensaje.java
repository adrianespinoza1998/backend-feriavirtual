package com.feriavirtual.apirest.models;

public class Mensaje {

	private String msg;
	
	public Mensaje() {
		
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	@Override
	public String toString() {
		return "Mensaje{" +
				"msg='" + msg + '\'' +
				'}';
	}
}
